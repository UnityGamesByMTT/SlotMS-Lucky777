    //delete all lines
    internal void ResetLines()
    {
        for (int i = 0; i < linePath.Count; i++)
        {
            StopCoroutine(linePath[i]);
        }
        foreach (Transform child in LineContainer)
        {
            Destroy(child.gameObject);
        }
    }
